import java.util.*;
public class pattern14 {
    public static void main(String[] args) {
        Scanner scn = new Scanner(System.in);
        int r = scn.nextInt();
        for (int i=1;i<=10;i++){
              int mul=r*i;
              System.out.println(r+"*"+i+"="+mul);
        }
    }
}
